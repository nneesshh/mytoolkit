CConfigHub::~CConfigHub() {
	IConfig *pConfig;
	int i;
	for (i = 0; i < _registeredConfigMax; ++i) {
		pConfig = _arrConfig[i];
		if (pConfig) {
			delete pConfig;
		}
	}
}

